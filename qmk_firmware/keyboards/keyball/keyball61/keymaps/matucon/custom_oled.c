// 数値を文字列に変換します。
static const char *itoc(uint8_t number) {
    static char str[5]; 
    uint8_t i = 0;

    do {
        str[i++] = number % 10 + '0';
        number /= 10;
    } while (number != 0);

    int len = i;
    for (int j = 0; j < len / 2; j++) {
        char temp = str[j];
        str[j] = str[len - j - 1];
        str[len - j - 1] = temp;
    }

    str[i] = '\0';
    return str;
}

// 数値を4桁固定の文字列に変換します。
static const char *format_u3d(uint8_t d) {
    static char buf[4] = {0}; // max width (3) + NUL (1)
    char        lead   = ' ';

    buf[2] = (d % 10) + '0';
    d /= 10;
    if (d == 0) {
        buf[1] = lead;
        lead   = ' ';
    } else {
        buf[1] = (d % 10) + '0';
        d /= 10;
    }
    if (d == 0) {
        buf[0] = lead;
    } else {
        buf[0] = (d % 10) + '0';
        d /= 10;
    }
    return buf;
}

static void print_cpi_status(void) {
    oled_write_raw_P(img_title, sizeof(img_title));
    oled_set_cursor(0, 2);

    oled_write(itoc(keyball_get_cpi()), false);
    oled_write_P(PSTR(" "), false);
    
    oled_set_cursor(4, 2);
    oled_write_char('0' + keyball_get_scroll_div(), false);
}

